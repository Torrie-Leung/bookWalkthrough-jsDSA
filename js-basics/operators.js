//arithmetic operator
let num = 0;
num = num+2;
num = num*2;
num = num/2;
num++;
num--;

//assignment operator
num+=1;
num-=2;
num*=3;
num/=2;
num%=3;
console.log(num)

//comparison operator
console.log('num == 1: '+(num == 1))
console.log('num === 1: '+(num === 1))
console.log('num != 1: '+(num != 1))
console.log('num >1: '+(num > 1))
console.log('num <1: '+(num < 1))

//logical operator
console.log('true && false: '+(true && false));
console.log('true || false: '+(true || false));
console.log('!true: '+(!true));